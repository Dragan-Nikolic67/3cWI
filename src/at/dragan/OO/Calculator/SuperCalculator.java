package at.dragan.OO.Calculator;

public class SuperCalculator extends Calculator{
    public double cos(int a){
        double cosValue = Math.cos(a);
        return cosValue;
    }
    public double sin(int a){
        double sinValue = Math.sin(a);
        return sinValue;
    }
    public double sqrt(int a){
        double sqrtValue = Math.sqrt(a);
        return sqrtValue;
    }
}
